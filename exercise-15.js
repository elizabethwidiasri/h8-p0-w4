function highestScore(students) {
    var final = {}
    if(students.length === 0) {
      return final
    }
    for (var i = 0; i < students.length; i++) {
        var temp = {}
        temp.name = students[i].name
        temp.score = students[i].score
        if (final[students[i].class] === undefined) {
            final[students[i].class] = {}
            if (students[i].score >= students[i].score) {
                final[students[i].class] = temp
            }
        }
    }
    return final
}

